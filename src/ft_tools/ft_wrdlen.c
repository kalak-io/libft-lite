#include <stddef.h>

size_t		ft_wrdlen(char const *s, char c)
{
	char *p;

	while (*s && *s == c)
		++s;
	p = (char*)s;
	while (*p && *p != c)
		++p;
	return ((size_t)(p - (char*)s));
}
